def NTSTATUS_FROM_WIN32(x: int) -> int:
    if x <= 0:
        return x
    else:
        return NTSTATUS((x & 0x0000FFFF) | (FACILITY_NTWIN32 << 16) | ERROR_SEVERITY_ERROR).value
